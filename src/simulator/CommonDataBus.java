package simulator;

public class CommonDataBus {
	private short data;
	private String source;
	
	public short getData() {
		return data;
	}
	public void setData(short data) {
		this.data = data;
	}
	public String getSource() {
		return source;
	}
	public void setSource(String source) {
		this.source = source;
	}
}